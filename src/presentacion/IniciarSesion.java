    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        // TODO add your handling code here:
        String u = "";
        String p = "";
        
        u = TextUser.getText();
        p = String.valueOf(TextPass.getPassword());
        if(TextUser.getText().equals("") || TextPass.getText().equals("")){
        JOptionPane.showMessageDialog(this, "Usuario y clave requerido!", 
                    "ERROR", JOptionPane.WARNING_MESSAGE);
    }else{
        if(dtu.validarUsuario(u, p)){
            user = dtu.getUsuario(u);
            rol = dtr.getRolByID(user.getRolID());
            Principal frmP = new Principal();
            frmP.setVisible(true);
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(this, 
            "Usuario o clave incorrecta!", 
            "ERROR", JOptionPane.ERROR_MESSAGE); 
            this.limpiarCampos();
            this.TextUser.grabFocus();
        }
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

